package mensagens;

/**
 *
 * Classe POJO que reresenta uma mensagem do tipo E-mail.
 */
public class Email {

    //Atributos
    private String mensagem;

    //Construtor
    public Email() {
    }

    //Metodos
    public String getMensagem() {
        return mensagem;
    }

    public void setMensagem(String mensagem) {
        this.mensagem = mensagem;
    }

}
